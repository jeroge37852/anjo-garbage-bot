def get_item_note(query: str) -> str | None:
    """
    品目名から個別注意事項を返す。

    引数:
        query: ユーザーが入力した品目名

    戻り値:
        個別注意事項（str）、該当なしの場合は None
    """
    nq = _normalize(query)
    for entry in ITEM_NOTES:
        for keyword in entry["keywords"]:
            if _normalize(keyword) in nq or nq in _normalize(keyword):
                return entry["note"]
    return None

