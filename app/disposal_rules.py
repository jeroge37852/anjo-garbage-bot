import unicodedata


def _normalize(text: str) -> str:
    text = unicodedata.normalize('NFKC', text)
    text = ''.join(chr(ord(c) - 0x60) if 'ァ' <= c <= 'ン' else c for c in text)
    return text.lower().strip()


